import torch
import torch.nn as nn
import torch.nn.functional as F
from torch_geometric.nn import GCNConv
from torch_geometric.data import Data
from sklearn.neighbors import NearestNeighbors
from sklearn.preprocessing import LabelEncoder
from skimage.measure import regionprops

device = 'cuda' if torch.cuda.is_available() else 'cpu'

def build_graph(counts, centers_xy, k=8):
    x = counts.astype(np.float32)
    x = np.log1p(x / x.sum(axis=1, keepdims=True).clip(min=1) * 1e4)
    n = len(x)
    nn_ = NearestNeighbors(n_neighbors=min(k+1, n)).fit(centers_xy)
    _, idx = nn_.kneighbors(centers_xy)
    src = np.repeat(np.arange(n), idx.shape[1] - 1)
    dst = idx[:, 1:].reshape(-1)
    edge_index = torch.tensor(np.stack([src, dst]), dtype=torch.long)
    return Data(x=torch.tensor(x), edge_index=edge_index)

class GCN(nn.Module):
    def __init__(self, in_dim, hidden, out_dim):
        super().__init__()
        self.c1 = GCNConv(in_dim, hidden)
        self.c2 = GCNConv(hidden, out_dim)
    def forward(self, d):
        h = F.relu(self.c1(d.x, d.edge_index))
        return self.c2(h, d.edge_index)

# Build per-FOV training graphs
