X_all_gcn = adata_gcn.X.toarray() if hasattr(adata_gcn.X, 'toarray') else np.asarray(adata_gcn.X)
X_all_gcn = np.asarray(X_all_gcn, dtype=np.float32)

train_data, train_y = [], {lvl: [] for lvl in LEVELS}
for fov in ['FOV_160', 'FOV_107']:
    rows = adata_gcn.obs.fov == fov
    Xc = X_all_gcn[rows.values]
    cxy = adata_gcn.obs.loc[rows, ['center_x', 'center_y']].values
    train_data.append(build_graph(Xc, cxy))
    for lvl in LEVELS:
        train_y[lvl].append(adata_gcn.obs[lvl].values[rows.values])

# Train one GCN per level
gcn_models, gcn_encoders = {}, {}
for lvl in LEVELS:
    y_all = np.concatenate(train_y[lvl])
    le = LabelEncoder().fit(y_all)
    gcn_encoders[lvl] = le
    offset = 0
    x_list, e_list, y_list = [], [], []
    for d, ys in zip(train_data, train_y[lvl]):
        x_list.append(d.x)
        e_list.append(d.edge_index + offset)
        y_list.append(torch.tensor(le.transform(ys), dtype=torch.long))
        offset += d.x.shape[0]
    big = Data(x=torch.cat(x_list), edge_index=torch.cat(e_list, dim=1)).to(device)
    y_big = torch.cat(y_list).to(device)
    model = GCN(big.x.shape[1], 64, len(le.classes_)).to(device)
    opt = torch.optim.Adam(model.parameters(), lr=5e-3)
    for _ in range(150):
        model.train(); opt.zero_grad()
        loss = F.cross_entropy(model(big), y_big)
        loss.backward(); opt.step()
    gcn_models[lvl] = model
    print(f'{lvl}: GCN trained')

