# Storage for per-method ARI numbers — fills in as we run each section.
RESULTS: dict[str, dict[str, float]] = {}

def evaluate_segmentation(name: str, pred_mask: np.ndarray,
                          cell_classifier, fov: str = 'FOV_122'):
    '''
    pred_mask: (2048, 2048) int — 0=bg, >0=cell id
    cell_classifier(counts: np.ndarray (n_cells, n_genes)) -> list of dicts
        each dict has keys: class_label, subclass_label, supertype_label, cluster_label
    '''
    spots = pd.read_csv(DATA_DIR / 'ground_truth' / 'spots_train.csv')
    spots = spots[spots.fov == fov].copy().reset_index(drop=True)
    counts, mask_ids = spots_to_cell_counts(pred_mask, spots)
    if len(mask_ids) == 0:
        print(f'[{name}] no spots assigned — all background prediction')
        preds = {lvl: np.array(['background'] * len(spots)) for lvl in LEVELS}
    else:
        cell_preds = cell_classifier(counts)
        cell_label_map = {int(mid): cell_preds[i] for i, mid in enumerate(mask_ids)}
        preds = project_to_spots(spots, pred_mask, cell_label_map)

    # Reuse cached GT for FOV_122
    if fov == 'FOV_122':
        gt = gt_122
    else:
        _, gt, _, _ = gt_spot_labels(fov)
    ari = score_predictions(gt, preds)
    ari['mean'] = float(np.mean(list(ari.values())))
    RESULTS[name] = ari
    print(f'[{name}]')
    for lvl, v in ari.items():
        print(f'  {lvl:18s} ARI = {v:+.3f}')
    return preds, ari

