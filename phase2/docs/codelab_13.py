# --- 1. CLAHE preprocessing ---
def to_rgb_uint8_clahe(img):
    v_lo, v_hi = np.percentile(img, (1, 99))
    x = np.clip((img.astype(np.float32) - v_lo) / max(v_hi - v_lo, 1), 0, 1)
    x = exposure.equalize_adapthist(x, clip_limit=0.02, kernel_size=128)
    return np.stack([(x * 255).astype(np.uint8)] * 3, axis=-1)

