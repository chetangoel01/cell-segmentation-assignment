# --- image loading ---
def load_fov(fov: str):
    '''Return (dapi, polyt) at middle z as 2048x2048 uint16.'''
    data = np.load(DATA_DIR / 'images' / f'{fov}_images.npz')
    return data['dapi'], data['polyt']

# --- µm <-> pixel conversion ---
def um_to_pix(fov: str, x_um, y_um):
    '''Global µm coords -> (row, col) pixel coords inside the 2048x2048 FOV.'''
    ox, oy = FOV_META.loc[fov, ['fov_x', 'fov_y']]
    ps = FOV_META.loc[fov, 'pixel_size']
    col = (np.asarray(x_um) - ox) / ps
    row = (np.asarray(y_um) - oy) / ps
    return row, col

# --- GT mask from polygon boundaries ---
def rasterize_gt_mask(fov: str, z: int = 2) -> tuple[np.ndarray, dict]:
    '''Build a 2048x2048 int mask from cell_boundaries.csv at the given z.
    Returns (mask, id_lookup) where id_lookup[mask_id] = original cell_id.'''
    boundaries = pd.read_csv(DATA_DIR / 'ground_truth' / 'cell_boundaries.csv',
                             index_col=0)
    labels = pd.read_csv(DATA_DIR / 'ground_truth' / 'cell_labels.csv')
    labels = labels[labels.fov == fov].set_index('cell_id')
    # Only cells whose centroid is in this FOV
    cells_here = boundaries.loc[boundaries.index.astype(str).isin(labels.index.astype(str))]

    mask = np.zeros((2048, 2048), dtype=np.int32)
    id_lookup = {}
    bx_col = f'boundaryX_z{z}'
    by_col = f'boundaryY_z{z}'

    for i, (cid, row) in enumerate(cells_here.iterrows(), start=1):
        xs = row[bx_col]
        ys = row[by_col]
        if pd.isna(xs) or pd.isna(ys):
            continue
        xs = np.array([float(v) for v in str(xs).split(',') if v.strip()])
        ys = np.array([float(v) for v in str(ys).split(',') if v.strip()])
        if len(xs) < 3:
            continue
        r, c = um_to_pix(fov, xs, ys)
        rr, cc = sk_polygon(r, c, shape=mask.shape)
        mask[rr, cc] = i
        id_lookup[i] = str(cid)
    return mask, id_lookup

# --- spots -> cell expression vector ---
GENE_LIST = ad.read_h5ad(DATA_DIR / 'ground_truth' / 'counts.h5ad').var_names.tolist()
GENE_INDEX = {g: i for i, g in enumerate(GENE_LIST)}

def spots_to_cell_counts(mask: np.ndarray, spots: pd.DataFrame) -> tuple[np.ndarray, np.ndarray]:
    '''Aggregate spots into cell x gene counts using mask[image_row, image_col].
    Returns (counts, cell_ids) where counts is (n_cells, n_genes) and cell_ids
    is the array of mask integer IDs (excluding 0 = background).'''
    rows = spots['image_row'].clip(0, mask.shape[0]-1).astype(int).values
    cols = spots['image_col'].clip(0, mask.shape[1]-1).astype(int).values
    cids = mask[rows, cols]
    keep = cids > 0
    if not keep.any():
        return np.zeros((0, len(GENE_LIST)), dtype=np.int32), np.array([], dtype=np.int32)
    cids = cids[keep]
    genes = spots['target_gene'].values[keep]
    gix = np.array([GENE_INDEX.get(g, -1) for g in genes])
    valid = gix >= 0
    cids, gix = cids[valid], gix[valid]

    unique_ids, inv = np.unique(cids, return_inverse=True)
    counts = np.zeros((len(unique_ids), len(GENE_LIST)), dtype=np.int32)
    np.add.at(counts, (inv, gix), 1)
    return counts, unique_ids

# --- spot-level GT labels for a FOV ---
def gt_spot_labels(fov: str, z: int = 2):
    '''Return spots_df + 4 GT label arrays per spot for the given FOV.
    Spots whose pixel falls in mask==0 get background.'''
    spots = pd.read_csv(DATA_DIR / 'ground_truth' / 'spots_train.csv')
    spots = spots[spots.fov == fov].copy().reset_index(drop=True)
    mask, lookup = rasterize_gt_mask(fov, z=z)
    rows = spots['image_row'].clip(0, mask.shape[0]-1).astype(int).values
    cols = spots['image_col'].clip(0, mask.shape[1]-1).astype(int).values
    mask_ids = mask[rows, cols]

    labels = pd.read_csv(DATA_DIR / 'ground_truth' / 'cell_labels.csv').set_index('cell_id')
    out = {lvl: np.array(['background'] * len(spots), dtype=object) for lvl in LEVELS}
    for i, mid in enumerate(mask_ids):
        if mid == 0:
            continue
        cid = lookup.get(mid)
        if cid is None or cid not in labels.index:
            continue
        for lvl in LEVELS:
            v = labels.loc[cid, lvl]
            if isinstance(v, pd.Series):
                v = v.iloc[0]
            out[lvl][i] = str(v)
    return spots, out, mask, lookup

# --- spot-level prediction from a (mask, per-cell-label-dict) ---
def project_to_spots(spots: pd.DataFrame, pred_mask: np.ndarray,
                    cell_labels: dict[int, dict[str, str]]) -> dict[str, np.ndarray]:
    '''cell_labels[mask_id][level] -> predicted label string. Spots in mask==0
    get background.'''
    rows = spots['image_row'].clip(0, pred_mask.shape[0]-1).astype(int).values
    cols = spots['image_col'].clip(0, pred_mask.shape[1]-1).astype(int).values
    mids = pred_mask[rows, cols]
    out = {lvl: np.array(['background'] * len(spots), dtype=object) for lvl in LEVELS}
    for i, mid in enumerate(mids):
        if mid <= 0 or mid not in cell_labels:
            continue
        for lvl in LEVELS:
            out[lvl][i] = cell_labels[mid].get(lvl, 'background')
    return out

def score_predictions(gt: dict[str, np.ndarray], pred: dict[str, np.ndarray]) -> dict[str, float]:
    return {lvl: adjusted_rand_score(gt[lvl], pred[lvl]) for lvl in LEVELS}

