"""# Hierarchy-aware XGBoost

The host hint: the kNN baseline scores ~flat across all 4 levels because its
errors are correlated. A **top-down conditional** classifier should win at
finer levels.

Pipeline: predict `class_label`, then concatenate the predicted class as a
feature when predicting `subclass_label`, and so on.
"""

import xgboost as xgb
from sklearn.preprocessing import LabelEncoder

# Train data = the 147 labeled cells. Use log-normalized counts.
adata_full_xgb = ad.read_h5ad(DATA_DIR / 'ground_truth' / 'counts.h5ad')
X_all = adata_full_xgb.X if not hasattr(adata_full_xgb.X, 'toarray') else adata_full_xgb.X.toarray()
X_all = np.asarray(X_all, dtype=np.float32)
X_all = X_all / X_all.sum(axis=1, keepdims=True).clip(min=1) * 1e4
X_all = np.log1p(X_all)
train_idx = np.where(adata_full_xgb.obs.fov.isin(['FOV_160', 'FOV_107']).values)[0]
Xtr = X_all[train_idx]

encoders, models_h = {}, {}
prev = None
for lvl in LEVELS:
  y = adata_full_xgb.obs[lvl].values[train_idx]
  le = LabelEncoder().fit(y)
  feats = Xtr if prev is None else np.hstack([Xtr, prev])
  clf = xgb.XGBClassifier(
      n_estimators=200, max_depth=4, learning_rate=0.1,
      n_jobs=4, tree_method='hist', eval_metric='mlogloss',
  )
  clf.fit(feats, le.transform(y))
  encoders[lvl] = le
  models_h[lvl] = clf
  prev = clf.predict_proba(feats)
  print(f'{lvl}: trained ({len(le.classes_)} classes)')

def hxgb_classifier(counts):
  x = counts.astype(np.float32)
  x = np.log1p(x / x.sum(axis=1, keepdims=True).clip(min=1) * 1e4)
  out_per_level, prev = {}, None
  for lvl in LEVELS:
      feats = x if prev is None else np.hstack([x, prev])
      pred = models_h[lvl].predict(feats)
      out_per_level[lvl] = encoders[lvl].inverse_transform(pred)
      prev = models_h[lvl].predict_proba(feats)
  return [{lvl: out_per_level[lvl][i] for lvl in LEVELS} for i in range(len(x))]

